    private void groupRadioButtons() {
        
        this.btnOptionsGroup.add(rbRegister);
        this.btnOptionsGroup.add(rbSignIn);
        
        this.rbRegister.addActionListener(rbActionListener);
        this.rbSignIn.addActionListener(rbActionListener);
    }
    
    private void initializeRadioButtonActionListener() {
       rbActionListener = (ActionEvent actionEvent) -> {
            JRadioButton aButton = (JRadioButton) actionEvent.getSource();
            
            if (aButton == rbRegister) {
                // Option Register is selected
                this.tfRegUserName.setText("");
                this.pfRegPassword.setText("");
                this.pfRegConfirmPwd.setText("");
                EnableComponents(pnlLogin, false);
                EnableComponents(pnlRegister, true);                
                
            } else if (aButton == rbSignIn) {
                // Option Sign In is selected
                this.tfLoginUserName.setText("");
                this.pfLoginPwd.setText("");
                EnableComponents(pnlRegister, false);
                EnableComponents(pnlLogin, true);
            }
        }; 
    }
    
