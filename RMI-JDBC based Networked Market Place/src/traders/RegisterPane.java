    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        
        String strUserName = this.tfLoginUserName.getText();
        String strPassWord = new String(this.pfLoginPwd.getPassword());
        if(!"".equals(strUserName) && !"".equals(strPassWord)) {
            profileInfo.setTraderName(strUserName);
            profileInfo.setPassword(strPassWord);
            
            // Call method on the Main GUI class to invoke Remote method on server
            boolean bRetValue = mainGUI.connectToMarket(profileInfo, false);
            if(bRetValue) {
                mainGUI.setStatus("Happy Trading...");
                EnableComponents(pnlRegister, false);
                EnableComponents(pnlLogin, false);                
            }
        } else {
            this.lblLoginEntryStatus.setText("Username/Password can't be empty");
        }
    }//GEN-LAST:event_btnLoginActionPerformed

