    private boolean isPasswordCorrect() {
        
        boolean isCorrect = true;
        
        char[] correctPassword = this.pfRegPassword.getPassword();
        char[] enteredPassword = this.pfRegConfirmPwd.getPassword();
        
        if (enteredPassword.length != correctPassword.length) {
            isCorrect = false;
        } else {
            isCorrect = Arrays.equals(enteredPassword, correctPassword);
        }

        //Zero out the password
        Arrays.fill(correctPassword,'0');
        Arrays.fill(enteredPassword, '0');

        return isCorrect;
    }
   
    public void enableLoginPanel() {
        
        this.tfLoginUserName.setText("");
        this.pfLoginPwd.setText("");
        EnableComponents(pnlLogin, true);
    }
    
    private void btnRegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegisterActionPerformed
        
        if (isPasswordCorrect()) {
            this.lblPwdStatus.setText("Passwords matched");
            profileInfo.setTraderName(this.tfRegUserName.getText());
            profileInfo.setPassword(new String(this.pfRegPassword.getPassword()));
            
            // Call method on the Main GUI class to invoke Remote method on server
            boolean bRetValue = mainGUI.connectToMarket(profileInfo, true);            
            if(bRetValue) {                
                mainGUI.setStatus("Login to continue...");
                EnableComponents(pnlRegister, false);
                EnableComponents(pnlLogin, true);
            }
        } else {
            this.pfRegPassword.setText("");
            this.pfRegConfirmPwd.setText("");
            this.lblPwdStatus.setText("Passwords do not match");
        }
    }//GEN-LAST:event_btnRegisterActionPerformed

